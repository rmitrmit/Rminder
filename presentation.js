document.getElementById("teamProfileBtn").addEventListener("click", function () {
    var content = document.getElementById("teamProfileContent");
    if (content.style.display === "none" || content.style.display === "") {
      content.style.display = "block";
    } else {
      content.style.display = "none";
    }
  });

